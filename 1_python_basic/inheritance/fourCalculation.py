"""A simple class exercise for arithmetic operation"""
class FourCalculation:
    def __init__(self,num1,num2):
        """Object creation"""
        self.num1 = num1
        self.num2 = num2

    def add(self):
        """Addition"""
        return self.num1 + self.num2

    def substract(self):
        """Substraction"""
        return self.num1 - self.num2

    def multiply(self):
        """Multiplication"""
        return self.num1 * self.num2

    def division(self):
        """Division"""
        return self.num1/self.num2
    

